public class PdfSacopDetailRequest {

	private String sacopsNumber;
	private String sacopsDescription;
	private String sacopsDate;

	public String getSacopsNumber() {
		return sacopsNumber;
	}

	public void setSacopsNumber(String sacopsNumber) {
		this.sacopsNumber = sacopsNumber;
	}

	public String getSacopsDescription() {
		return sacopsDescription;
	}

	public void setSacopsDescription(String sacopsDescription) {
		this.sacopsDescription = sacopsDescription;
	}

	public String getSacopsDate() {
		return sacopsDate;
	}

	public void setSacopsDate(String sacopsDate) {
		this.sacopsDate = sacopsDate;
	}

}
